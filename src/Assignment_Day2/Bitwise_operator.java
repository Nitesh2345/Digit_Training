package Assignment_Day2;

public class Bitwise_operator {
    public static void main(String[] args) {
        int a=4;
        int b=8;
        int Bitwise_And= a&b;
        int Bitwise_or= a|b;
        int Bitwise_XOR= a^b;
        System.out.println(Bitwise_And+" ,"+Bitwise_or+", "+ Bitwise_XOR);
    }
}
